<h2>PERATURAN PELAKSANAAN UJIAN</h2>
<ol>
    <li>Peserta harus mengerjakan tes secara mandiri dan tidak dibantu oleh orang lain.</li>
    <li>Peserta tidak diperbolehkan mendokumentasikan dan menyebarluaskan soal tes.</li>
    <li>Peserta tidak diperbolehkan menyimpan seluruh panduan dan dokumen pendukung tes setelah selesai melaksanakan tes.</li>
    <li>Panitia berhak menghentikan proses tes yang sedang berlangsung atau mendiskualifikasi jika mendapati peserta melakukan kecurangan atau melanggar peraturan.</li>
    <li>Peserta <strong>dilarang menggunakan</strong> alat bantu seperti <strong>kamus online maupun offline</strong> selama mengerjakan tes.</li>
</ol>