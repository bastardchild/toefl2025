    <h2>LISTENING TEST</h2>
    <h3>SECTION 1</h3>
    <h4>LISTENING COMPREHENSION</h4>
    <p>Time—approximately 35 minutes (including the reading of the directions for each part)</p>
    <p>In this section of the test, you will have an opportunity to demonstrate your ability to understand conversations and talks in English. There are three parts to this section. Answer all the questions on the basis of what is stated or implied by the speakers you hear. Do not take notes or write in your test book at any time. Do not turn the pages until you are told to do so.</p>
    <h4>Part A</h4>
    <p>Directions: In Part A you will hear short conversations between two people. After each conversation, you will hear a question about the conversation. The conversations and questions will not be repeated. After you hear a question, read the four possible answers in your test book and choose the best answer. Then, on your answer sheet, find the number of the question and fill in the space that corresponds to the letter of the answer you have chosen.</p>
    <p>Listen to an example:</p>
    <p>(man)<br>
    <em>That exam was just awful.</em></p>
    <p>(woman)<br>
    <em>Oh, it could have been worse.</em></p>
    <p>(narrator)<br>
    <em>What does the woman mean?</em></p>
    <p>In your test book, you read:</p>
    <ul>
        <li>(A) The exam was really awful.</li>
        <li>(B) It was the worst exam she had ever seen.</li>
        <li>(C) It couldn't have been more difficult.</li>
        <li>(D) It wasn't that hard.</li>
    </ul>
    <p>You learn from the conversation that the man thought the exam was very difficult and that the woman disagreed with the man. The best answer to the question, "What does the woman mean?" is (D), "It wasn't that hard." Therefore, the correct choice is (D).</p>
